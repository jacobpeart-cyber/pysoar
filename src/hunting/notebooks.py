@dataclass
class NotebookCell:
    """Represents a cell in a hunt notebook"""

    cell_type: str  # markdown, query, result, visualization, code
    content: str
    output: Optional[str] = None
    executed_at: Optional[datetime] = None
    execution_time_ms: Optional[float] = None
    metadata: dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> dict:
        """Convert cell to dictionary"""
        return {
            "cell_type": self.cell_type,
            "content": self.content,
            "output": self.output,
            "executed_at": self.executed_at.isoformat() if self.executed_at else None,
            "execution_time_ms": self.execution_time_ms,
            "metadata": self.metadata,
        }

    @classmethod
    def from_dict(cls, data: dict) -> "NotebookCell":
        """Create cell from dictionary"""
        if data.get("executed_at"):
            data["executed_at"] = datetime.fromisoformat(data["executed_at"])
        return cls(**data)


